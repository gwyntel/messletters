def convert(text: str, style: str) -> str:
    """Convert text to a Unicode fancy style.

    Args:
        text: Input text (ASCII letters and digits work best)
        style: Style name from STYLES dict

    Returns:
        Converted text string, or original text if style not found
    """
    if style not in STYLES:
        return text
    lower_cps, upper_cps, digit_cps = STYLES[style]
    result = []
    for ch in text:
        cp = ord(ch)
        if 97 <= cp <= 122:  # a-z
            result.append(chr(lower_cps[cp - 97]))
        elif 65 <= cp <= 90:  # A-Z
            result.append(chr(upper_cps[cp - 65]))
        elif 48 <= cp <= 57:  # 0-9
            result.append(chr(digit_cps[cp - 48]))
        else:
            result.append(ch)
    return ''.join(result)


def list_styles() -> list:
    """Return all available style names sorted alphabetically."""
    return sorted(STYLES.keys())


def preview_styles(text: str = "Hello World") -> list:
    """Preview all styles with the given text.

    Returns:
        List of (style_name, converted_text) tuples
    """
    return [(name, convert(text, name)) for name in sorted(STYLES.keys())]


def search_styles(query: str) -> list:
    """Search for styles by name (case-insensitive substring match).

    Args:
        query: Search string

    Returns:
        List of matching style names
    """
    q = query.lower()
    return [name for name in sorted(STYLES.keys()) if q in name.lower()]


def reverse_lookup(char: str) -> list:
    """Find which styles map a given ASCII character and what they produce.

    Args:
        char: Single ASCII character

    Returns:
        List of (style_name, mapped_char) tuples
    """
    if len(char) != 1:
        return []
    cp = ord(char)
    results = []
    for name in sorted(STYLES.keys()):
        lower_cps, upper_cps, digit_cps = STYLES[name]
        if 97 <= cp <= 122:
            mapped = chr(lower_cps[cp - 97])
        elif 65 <= cp <= 90:
            mapped = chr(upper_cps[cp - 65])
        elif 48 <= cp <= 57:
            mapped = chr(digit_cps[cp - 48])
        else:
            continue
        results.append((name, mapped))
    return results


